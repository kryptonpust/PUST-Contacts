    private String admin;

    private String dc;
    private String departments;
    private String faculties;
    private String menu;
    private String officers;
    private String offices;
    private String residence;
    private String teachers;
    private String ac;
    private String fc;
    private String rb;
    private String not;
    private String noc;
    private String rnot;

